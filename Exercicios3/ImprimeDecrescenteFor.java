/*
8 3 Imprima os números de 10 a 1 em ordem decrescente.
Atenção, utilize um loop FOR.
*/

public class ImprimeDecrescenteFor {
    public static void main(String[] args) {
        int i = 10;
        while(i >= 1){
            System.out.print(i);
            System.out.print(" ");
            i--;
        }

    }
}